/**
 * Write a program that declares three single-dimensional arrays named miles, gallons, and mpg. Each
 * array should be capable of holding 10 elements. 
 * 
 * In the miles array, store the numbers
 * 240.5, 300.0, 189.6, 310.6 280.7, 216.9, 199.4, 160.3, 177.4, 192.3
 * 
 * In the gallons array, store the numbers
 * 10.3, 15.6, 9.7, 14, 16.3, 15.7, 14.9, 10.7, 9.3, 9.4.
 * 
 * Each element of the mpg array should be calculated as the corresponding element to the miles
 * array divided by the equivalent element of the gallons array; for example, 
 * mpg[0] = miles[0] / gallons[0].
 * 
 * Use pointers when calculating and displaying the elements of the mpg array.
*/

#include <stdio.h>

int main()
{
    #define NUMELEMS 10
    float miles[NUMELEMS] = { 240.5, 300.0, 189.6, 310.6, 280.7, 216.9, 199.4, 160.3, 177.4, 192.3 };
    float *milesPtr = miles;

    float gallons[NUMELEMS] = {10.3, 15.6, 9.7, 14, 16.3, 15.7, 14.9, 10.7, 9.3, 9.4};
    float *gallonsPtr = gallons;

    float mpg[NUMELEMS];
    float *mpgPtr = mpg;

    printf("%f\n", *milesPtr);
    printf("%f\n", *gallonsPtr);

    for(int i = 0; i < NUMELEMS; i++)
        *mpgPtr++ = *milesPtr++ / *gallonsPtr++;

    mpgPtr = mpg;

    while(mpgPtr < mpg + NUMELEMS)
        printf("%.2f ", *mpgPtr++);
    
    printf("\n");
    return 0;

}