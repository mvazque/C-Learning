#include <stdio.h>

int main(void) 
{
    printf("The decimal value of the letter 'a' is %d.\n", 'a');
    printf("The decimal value of the letter 'm' is %d.\n", 'm');
    printf("The decimal value of the letter 'n' is %d.\n", 'n');
    printf("The decimal value of the escape sequence '\\n' is %d.\n", '\n');
}