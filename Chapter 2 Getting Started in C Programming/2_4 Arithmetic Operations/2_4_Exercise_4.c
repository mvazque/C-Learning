#include <stdio.h>

int main(void)
{
    printf("The value of %d divided by %d is %d.\n", 15, 4, 15 / 4);
    printf("The remainder of %d divided by %d is %d.\n", 15, 4, 15 % 4);
    printf("The value of %d times %d minus the product of %d times %d is %d\n", 5, 3, 5, 4, 5 * 3 - (6 * 4));

    return 0;
}